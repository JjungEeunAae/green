package ex01;

public class Ex02 {
	public static void main(String[] args) {
		int a = 10;
		System.out.println("a = " + a);
		
		int b = 20;
		System.out.println("a + b = " + a + b);	// 문자열
		System.out.println("a + b = " + (a+b)); // 숫자
		
		String c = "a";
		String d = "b";
		System.out.println(c + d);
	}
}
